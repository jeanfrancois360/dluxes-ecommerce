/**
 * Get country configuration by ISO code
 * Returns default config for unknown countries
 */
export function getCountryConfig(code: string): CountryAddressConfig {
  const config = ADDRESS_COUNTRIES.find(c => c.code === code);

  // Default fallback for unknown countries
  // Assumes minimal requirements to avoid blocking checkout
  return config || {
    code: code || 'US',
    name: 'Unknown Country',
    flag: '',
    phonePrefix: '+1',
    showState: true,
    showPostalCode: true,
    requiresState: false,
    requiresPostalCode: false,
    stateLabel: 'State/Province',
    postalCodeLabel: 'Postal Code',
    region: 'Other',
  };
}

/**
 * Get popular countries (shown first in dropdown)
 */
export function getPopularCountries(): CountryAddressConfig[] {
  return ADDRESS_COUNTRIES.filter(c => c.popular).sort((a, b) => a.name.localeCompare(b.name));
}

/**
 * Get all countries alphabetically
 */
export function getAllCountries(): CountryAddressConfig[] {
  return [...ADDRESS_COUNTRIES].sort((a, b) => a.name.localeCompare(b.name));
}

/**
 * Get countries by region
 */
export function getCountriesByRegion(region: string): CountryAddressConfig[] {
  return ADDRESS_COUNTRIES
    .filter(c => c.region === region)
    .sort((a, b) => a.name.localeCompare(b.name));
}

/**
 * Search countries by name or code
 */
export function searchCountries(query: string): CountryAddressConfig[] {
  const lowerQuery = query.toLowerCase();
  return ADDRESS_COUNTRIES.filter(c =>
    c.name.toLowerCase().includes(lowerQuery) ||
    c.code.toLowerCase().includes(lowerQuery)
  );
}

/**
 * Validate postal code against country pattern
 */
export function validatePostalCode(postal: string, countryCode: string): boolean {
  const config = getCountryConfig(countryCode);

  // If country doesn't show postal code field, any value is valid
  if (!config.showPostalCode) return true;

  // If no pattern defined, any format is acceptable
  if (!config.postalCodePattern) return true;

  // Test against country-specific pattern
  return config.postalCodePattern.test(postal.trim());
}

/**
 * Format phone number with country prefix
 */
export function formatPhoneNumber(phone: string, countryCode: string): string {
  const config = getCountryConfig(countryCode);
  const digits = phone.replace(/\D/g, '');

  // Country-specific formatting
  if (countryCode === 'RW') {
    // Rwanda: +250 788 123 456
    return digits.replace(/(\d{3})(\d{3})(\d{3})/, '$1 $2 $3');
  } else if (countryCode === 'US' || countryCode === 'CA') {
    // US/Canada: +1 (555) 123-4567
    return digits.replace(/(\d{3})(\d{3})(\d{4})/, '($1) $2-$3');
  } else if (countryCode === 'GB') {
    // UK: +44 20 1234 5678
    return digits.replace(/(\d{2})(\d{4})(\d{4})/, '$1 $2 $3');
  } else if (countryCode === 'SG') {
    // Singapore: +65 1234 5678
    return digits.replace(/(\d{4})(\d{4})/, '$1 $2');
  } else if (countryCode === 'IN') {
    // India: +91 12345 67890
    return digits.replace(/(\d{5})(\d{5})/, '$1 $2');
  }

  // Default: just return digits
  return digits;
}

/**
 * Get phone placeholder for country
 */
export function getPhonePlaceholder(countryCode: string): string {
  const placeholders: Record<string, string> = {
    RW: '788 123 456',
    US: '(555) 123-4567',
    CA: '(555) 123-4567',
    GB: '20 1234 5678',
    NG: '803 123 4567',
    SG: '1234 5678',
    KE: '712 345 678',
    AE: '50 123 4567',
    ZA: '82 123 4567',
    IN: '12345 67890',
    AU: '412 345 678',
    FR: '6 12 34 56 78',
    DE: '151 23456789',
  };

  return placeholders[countryCode] || '123456789';
}

/**
 * Validate phone number against country pattern
 */
export function validatePhoneNumber(phone: string, countryCode: string): boolean {
  const config = getCountryConfig(countryCode);
  const digits = phone.replace(/\D/g, '');

  // If country has specific pattern, use it
  if (config.phonePattern) {
    return config.phonePattern.test(digits);
  }

  // Default: require at least 8 digits
  return digits.length >= 8 && digits.length <= 15;
}

/**
 * Get address format description for country
 */
export function getAddressFormatHelp(countryCode: string): string {
  const config = getCountryConfig(countryCode);

  const parts = ['Street address, building'];

  if (config.showState && config.requiresState) {
    parts.push(config.stateLabel.toLowerCase());
  }

  parts.push('city');

  if (config.showPostalCode && config.requiresPostalCode) {
    parts.push(config.postalCodeLabel.toLowerCase());
  }

  return `Include: ${parts.join(', ')}`;
}
